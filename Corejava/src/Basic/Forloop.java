package Basic;

public class Forloop {	
	public static void main(String[] args) {
		for ( int i=0;i<10;i++) {
			System.out.println("The value of I is "+i);
		}
		System.out.println();
		for (int i=10;i>0;i--) {
			System.out.println("The Reversed value of I is -> "+ i);
		}
		System.out.println();
		for ( int i=1;i<10;i++) {
			for (int j=1;j<5;j++) {
			System.out.println("J = "+j);
		}
	}
	}
}