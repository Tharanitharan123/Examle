package Wrapper;

public class Example {

		public static void main(String[] args) {
			//Autoboxing wrapper
			byte b=10;
			short s=20;
			int i=30;
			char c='a';
			boolean B=true;
		Byte bytea=b;
		Short shortt=s;
		Integer intk=i;
		Character charl=c;
		Boolean booleanh=B;
		System.out.println("printing object values");
		System.out.println("----------------------");
		System.out.println("Byte Object : " + bytea);
		System.out.println("Short Object : " + shortt);
		System.out.println("Integer Object : " + intk);
		System.out.println("Character object : " + charl);
		System.out.println("Boolean Object : " + booleanh);
		}

	}


